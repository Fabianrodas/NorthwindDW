package modelo;

public class Grafico3 {
    private String pais;
    private double total;

    public Grafico3(String pais, double total) {
        this.pais = pais;
        this.total = total;
    }

    public Grafico3() {
    }

    public String getPais() {
        return pais;
    }

    public void setPais(String pais) {
        this.pais = pais;
    }

    public double getTotal() {
        return total;
    }

    public void setTotal(double total) {
        this.total = total;
    }
    
    
}
